Ejercicio 4: Herencia con Empleado y Gerente
Objetivo: Aplicar herencia en una jerarquía de empleados.

Requisitos:

Crea una clase Empleado con las propiedades nombre y salario.

Crea un método calcularSueldoAnual().

Crea una subclase Gerente que herede de Empleado y tenga una propiedad adicional bonoAnual.

En Gerente, modifica calcularSueldoAnual() para incluir el bonoAnual

Si creas un Empleado con salario 2000 y llamas calcularSueldoAnual(), debe devolver:
Sueldo anual: 24000


Si creas un Gerente con salario 2000 y bono de 5000, debe devolver:
Sueldo anual: 29000
