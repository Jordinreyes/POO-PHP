Clase Coche con Métodos de Encapsulación
Objetivo: Crear una clase Coche con propiedades privadas y métodos para acceder a ellas.

Requisitos:

Crea una clase Coche con las propiedades marca, modelo y velocidad.

La velocidad debe iniciar en 0 y solo modificarse con los métodos acelerar() y frenar().

acelerar() debe incrementar la velocidad en 10.

frenar() debe reducir la velocidad en 10, sin bajar de 0.

Agrega un método mostrarEstado() que devuelva un mensaje con la marca, modelo y velocidad actual.