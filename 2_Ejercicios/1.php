Clase Car
Crea una clase Car que represente un automóvil con los siguientes atributos y métodos:
Atributos: marca, modelo, año
Constructor: Recibe y asigna los valores de los atributos.
Método getInfo(): Devuelve una cadena con la información del automóvil.
$auto = new Car("Toyota", "Corolla", 2022);
echo $auto->getInfo();
